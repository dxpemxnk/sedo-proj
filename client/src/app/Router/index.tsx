// КОНФИГУРАЦИЯ МАРШРУТИЗАЦИИ (Routing Configuration)
// Этот файл определяет все маршруты (URL-пути) приложения и соответствующие им компоненты

// Импорт функции создания браузерного роутера из React Router v6
// createBrowserRouter использует HTML5 History API для навигации без перезагрузки страницы
import { createBrowserRouter } from "react-router-dom";

// Импорт Layout компонента - общая оболочка для всех страниц (шапка, навигация, футер)
import { Layout } from "./Layout/Layout";

// Импорт всех страничных компонентов из индексного файла pages
// Каждая страница - это отдельный экран приложения
import { AuthPage, DocPage,CrePage,  RegPage, NotFound } from "@/pages";

// ЗАКОММЕНТИРОВАННЫЙ ИМПОРТ - возможно, страница редактирования документа (пока не используется)
// import { CurrentPage } from "@/pages/CurrentPage";

// ПЕРЕЧИСЛЕНИЕ МАРШРУТОВ (Enum) - централизованное хранение всех путей приложения
// Использование enum предотвращает опечатки в путях и обеспечивает типобезопасность
export enum CLIENT_ROUTES {
    HOME = '/',              // Главная страница (корневой путь)
    AUTH = '/auth',          // Страница авторизации (вход в систему)
    REG = '/reg',           // Страница регистрации нового пользователя
    DOCS = '/list',         // Страница со списком всех документов
    CREATE = '/docs',       // Страница создания нового документа
    UPDATE = "/docs/:id",   // Страница редактирования документа (:id - параметр URL)
    NOT_FOUND = '*',        // Catch-all роут для несуществующих страниц (404)
}

// СОЗДАНИЕ И ЭКСПОРТ КОНФИГУРАЦИИ РОУТЕРА
// createBrowserRouter принимает массив объектов с описанием маршрутов
export const router = createBrowserRouter([
    {
        // КОРНЕВОЙ МАРШРУТ - родительский для всех остальных
        path: CLIENT_ROUTES.HOME,
        
        // Layout - компонент-обёртка, который будет отображаться для всех дочерних маршрутов
        // В нём обычно находится шапка сайта, навигация, футер
        element: <Layout />,
        
        // ДОЧЕРНИЕ МАРШРУТЫ - вложенные внутри Layout
        // Содержимое этих страниц будет рендериться внутри <Outlet /> в Layout компоненте
        children:[
            // ЗАКОММЕНТИРОВАННЫЙ МАРШРУТ - возможно, главная страница (пока не реализована)
            // {
            //     path: CLIENT_ROUTES.HOME,
            //     element: <HomePage />,
            // },
            
            {
                // Маршрут для страницы авторизации (/auth)
                path: CLIENT_ROUTES.AUTH,
                element: <AuthPage />  // Компонент с формой входа в систему
            },
            {
                // Маршрут для страницы регистрации (/reg)
                path: CLIENT_ROUTES.REG,
                element: <RegPage />,  // Компонент с формой регистрации
            },
            {
                // Маршрут для страницы списка документов (/list)
                path: CLIENT_ROUTES.DOCS,
                element: <DocPage />,  // Компонент со списком всех документов пользователя
            },
            {
                // Catch-all маршрут - обрабатывает все несуществующие URL
                // Должен быть последним в списке маршрутов
                path: CLIENT_ROUTES.NOT_FOUND,
                element: <NotFound />,  // Компонент страницы 404 (страница не найдена)
            },
            {
                // Маршрут для создания нового документа (/docs)
                path: CLIENT_ROUTES.CREATE,
                element: <CrePage />,  // Компонент с формой создания документа
            },
            
            // ЗАКОММЕНТИРОВАННЫЙ МАРШРУТ - редактирование документа (пока не реализовано)
            // {
            //     path: CLIENT_ROUTES.UPDATE,  // /docs/:id где :id - ID документа
            //     element: <CurrentPage />,    // Компонент редактирования конкретного документа
            // },
        ]
    }
    // Здесь могут быть дополнительные корневые маршруты, если потребуется
]);
